using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace ModelGen;

// PROBE E — forward references. Same mechanism as probe A's InlineRecordGenerator,
// renamed to the CreateModel(...) marker the probe brief specifies. The author writes,
// as a VALUE:
//     CreateModel("Foo", ("Id", typeof(Guid)), ("Name", typeof(string)));
// and references `Foo` as a TYPE elsewhere. The generator scans every CreateModel(...)
// marker across the WHOLE compilation and back-fills `public record Foo(...)` into that
// same compilation. Because the scan is compilation-wide, a marker in file X is visible
// to a use in file Y regardless of file or textual order.
[Generator]
public sealed class CreateModelGenerator : IIncrementalGenerator
{
    private const string MarkerMethod = "CreateModel";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(ctx => ctx.AddSource(
            "CreateModel.g.cs", SourceText.From(MarkerSource, Encoding.UTF8)));

        var specs = context.SyntaxProvider.CreateSyntaxProvider(
                predicate: static (node, _) => IsCandidate(node),
                transform: static (ctx, _) => Extract(ctx))
            .Where(static s => s is not null)
            .Select(static (s, _) => s!)
            .Collect();

        context.RegisterSourceOutput(specs, static (ctx, all) => Emit(ctx, all));
    }

    private static bool IsCandidate(SyntaxNode node) =>
        node is InvocationExpressionSyntax inv && CalleeName(inv) == MarkerMethod;

    private static string? CalleeName(InvocationExpressionSyntax inv) => inv.Expression switch
    {
        IdentifierNameSyntax id => id.Identifier.ValueText,
        MemberAccessExpressionSyntax ma => ma.Name.Identifier.ValueText,
        _ => null,
    };

    private static ModelSpec? Extract(GeneratorSyntaxContext ctx)
    {
        var inv = (InvocationExpressionSyntax)ctx.Node;
        var args = inv.ArgumentList.Arguments;
        if (args.Count < 1)
            return null;

        var name = ConstString(ctx.SemanticModel, args[0].Expression);
        if (name is null)
            return null;

        var fields = new List<FieldSpec>();
        for (var i = 1; i < args.Count; i++)
        {
            if (args[i].Expression is not TupleExpressionSyntax tuple || tuple.Arguments.Count != 2)
                return null;

            var fieldName = ConstString(ctx.SemanticModel, tuple.Arguments[0].Expression);
            if (fieldName is null)
                return null;

            if (tuple.Arguments[1].Expression is not TypeOfExpressionSyntax typeOf)
                return null;

            var typeInfo = ctx.SemanticModel.GetTypeInfo(typeOf.Type).Type;
            var typeName = typeInfo is null || typeInfo.TypeKind == TypeKind.Error
                ? typeOf.Type.ToString()
                : typeInfo.ToDisplayString(FullyQualified);

            fields.Add(new FieldSpec(fieldName, typeName));
        }

        return new ModelSpec(name, fields.ToImmutableArray());
    }

    private static string? ConstString(SemanticModel model, ExpressionSyntax expr)
    {
        var value = model.GetConstantValue(expr);
        if (value.HasValue && value.Value is string s)
            return s;
        return expr is LiteralExpressionSyntax lit && lit.IsKind(SyntaxKind.StringLiteralExpression)
            ? lit.Token.ValueText
            : null;
    }

    private static void Emit(SourceProductionContext ctx, ImmutableArray<ModelSpec> specs)
    {
        var byName = new Dictionary<string, ModelSpec>();
        foreach (var spec in specs)
            byName[spec.Name] = spec;

        foreach (var spec in byName.Values)
        {
            var fields = string.Join(", ", spec.Fields.Select(f => $"{f.Type} {f.Name}"));
            var src =
$@"// <auto-generated/> — minted by CreateModelGenerator from a CreateModel(...) use-site.
#nullable enable
public record {spec.Name}({fields});
";
            ctx.AddSource($"Minted.{spec.Name}.g.cs", SourceText.From(src, Encoding.UTF8));
        }
    }

    private static readonly SymbolDisplayFormat FullyQualified =
        SymbolDisplayFormat.FullyQualifiedFormat;

    private const string MarkerSource =
@"// <auto-generated/>
#nullable enable
using System;

internal static class Models
{
    // The author declares a needed type inline, as a value. No runtime effect — the
    // point is purely the compile-time source-generation back-fill.
    public static void CreateModel(string name, params (string field, Type type)[] fields) { }
}
";
}

internal sealed record ModelSpec(string Name, ImmutableArray<FieldSpec> Fields);

internal sealed record FieldSpec(string Name, string Type);
