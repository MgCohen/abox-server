using System.Runtime.CompilerServices;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;

namespace SpikeGen;

// Standalone emit: reads the [Snippet] methods and writes a recipe node per snippet into
// spike/src/Nodes.Generated.cs. It depends only on the snippet SOURCE (parsed as text), not
// on spike/src's compilation, so there's no build cycle. Same shape as an in-build source
// generator later — only the host differs.
static class Program
{
    static readonly IFieldRecognizer[] Recognizers =
    [
        new ParamRecognizer(),
        new RefMarkerRecognizer(),
        new BodyMarkerRecognizer(),
        new BlockRecognizer(),
    ];

    static void Main()
    {
        var srcDir = Path.GetFullPath(Path.Combine(GenDir(), "..", "src"));
        var root = CSharpSyntaxTree.ParseText(File.ReadAllText(Path.Combine(srcDir, "Snippets.cs"))).GetRoot();

        var snippets = root.DescendantNodes().OfType<MethodDeclarationSyntax>()
            .Where(HasSnippet)
            .OrderBy(m => m.Identifier.ValueText)
            .ToList();

        var nodes = snippets.Select(m => Emitter.Node(m, Recognizers)).ToList();
        var nodesOut = "// <auto-generated/>\n#nullable enable\nnamespace Spike;\n\n"
            + string.Join("\n", nodes) + "\n";
        var nodesPath = Path.Combine(srcDir, "Nodes.Generated.cs");
        File.WriteAllText(nodesPath, nodesOut);

        var factories = snippets.Select(m => Emitter.Factory(m, Recognizers));
        var factoriesOut = "// <auto-generated/>\n#nullable enable\nnamespace Spike;\n\n"
            + "static partial class Recipe\n{\n" + string.Join("\n", factories) + "\n}\n";
        var factoriesPath = Path.Combine(srcDir, "Factories.Generated.cs");
        File.WriteAllText(factoriesPath, factoriesOut);

        Console.WriteLine($"generated {nodes.Count} recipe nodes -> {nodesPath}");
        Console.WriteLine($"generated {nodes.Count} factories -> {factoriesPath}");
        foreach (var n in nodes) Console.WriteLine("  " + n);
    }

    static bool HasSnippet(MethodDeclarationSyntax m) =>
        m.AttributeLists.SelectMany(a => a.Attributes)
            .Any(a => a.Name.ToString() is "Snippet" or "SnippetAttribute");

    static string GenDir([CallerFilePath] string path = "") => Path.GetDirectoryName(path)!;
}
