using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace InlineTypeGen;

// PROBE A — "usage drives generation". The author writes, as a VALUE:
//     CreateRecord("Foo", ("Id", typeof(Guid)), ("Name", typeof(string)));
// and then references `Foo` as a TYPE on a later line. This generator scans for
// those CreateRecord(...) markers and back-fills `public record Foo(...)` into the
// SAME compilation, so the downstream `Foo f = new Foo(...)` binds — additively,
// with no interceptors.
[Generator]
public sealed class InlineRecordGenerator : IIncrementalGenerator
{
    private const string MarkerMethod = "CreateRecord";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // 1) Emit the marker method itself so the use-site call compiles. It is a
        //    no-op at runtime; its only purpose is to be a legal value-expression
        //    the author writes to declare the need.
        context.RegisterPostInitializationOutput(ctx => ctx.AddSource(
            "CreateRecord.g.cs", SourceText.From(MarkerSource, Encoding.UTF8)));

        // 2) Find every CreateRecord("Name", (field, typeof(T))...) invocation and
        //    project it to a parsed RecordSpec using the semantic model (needed to
        //    turn `typeof(Guid)` into the fully-qualified `global::System.Guid`).
        var specs = context.SyntaxProvider.CreateSyntaxProvider(
                predicate: static (node, _) => IsCandidate(node),
                transform: static (ctx, _) => Extract(ctx))
            .Where(static s => s is not null)
            .Select(static (s, _) => s!)
            .Collect();

        context.RegisterSourceOutput(specs, static (ctx, all) => Emit(ctx, all));
    }

    private static bool IsCandidate(SyntaxNode node) =>
        node is InvocationExpressionSyntax inv && CalleeName(inv) == MarkerMethod;

    private static string? CalleeName(InvocationExpressionSyntax inv) => inv.Expression switch
    {
        IdentifierNameSyntax id => id.Identifier.ValueText,
        MemberAccessExpressionSyntax ma => ma.Name.Identifier.ValueText,
        _ => null,
    };

    private static RecordSpec? Extract(GeneratorSyntaxContext ctx)
    {
        var inv = (InvocationExpressionSyntax)ctx.Node;
        var args = inv.ArgumentList.Arguments;
        if (args.Count < 1)
            return null;

        var name = ConstString(ctx.SemanticModel, args[0].Expression);
        if (name is null)
            return null;

        var fields = new List<FieldSpec>();
        for (var i = 1; i < args.Count; i++)
        {
            if (args[i].Expression is not TupleExpressionSyntax tuple || tuple.Arguments.Count != 2)
                return null;

            var fieldName = ConstString(ctx.SemanticModel, tuple.Arguments[0].Expression);
            if (fieldName is null)
                return null;

            if (tuple.Arguments[1].Expression is not TypeOfExpressionSyntax typeOf)
                return null;

            var typeInfo = ctx.SemanticModel.GetTypeInfo(typeOf.Type).Type;
            var typeName = typeInfo is null || typeInfo.TypeKind == TypeKind.Error
                ? typeOf.Type.ToString()
                : typeInfo.ToDisplayString(FullyQualified);

            fields.Add(new FieldSpec(fieldName, typeName));
        }

        return new RecordSpec(name, fields.ToImmutableArray());
    }

    private static string? ConstString(SemanticModel model, ExpressionSyntax expr)
    {
        var value = model.GetConstantValue(expr);
        if (value.HasValue && value.Value is string s)
            return s;
        return expr is LiteralExpressionSyntax lit && lit.IsKind(SyntaxKind.StringLiteralExpression)
            ? lit.Token.ValueText
            : null;
    }

    private static void Emit(SourceProductionContext ctx, ImmutableArray<RecordSpec> specs)
    {
        // De-dup by name: the same CreateRecord may appear at several call sites
        // (and the incremental pipeline can present duplicates). Last writer wins;
        // a real impl would diagnose conflicting field lists.
        var byName = new Dictionary<string, RecordSpec>();
        foreach (var spec in specs)
            byName[spec.Name] = spec;

        foreach (var spec in byName.Values)
        {
            var fields = string.Join(", ", spec.Fields.Select(f => $"{f.Type} {f.Name}"));
            var src =
$@"// <auto-generated/> — minted by InlineRecordGenerator from a CreateRecord(...) use-site.
#nullable enable
public record {spec.Name}({fields});
";
            ctx.AddSource($"Minted.{spec.Name}.g.cs", SourceText.From(src, Encoding.UTF8));
        }
    }

    private static readonly SymbolDisplayFormat FullyQualified =
        SymbolDisplayFormat.FullyQualifiedFormat;

    private const string MarkerSource =
@"// <auto-generated/>
#nullable enable
using System;

internal static class InlineTypes
{
    // The author declares a needed type inline, as a value. Returns nothing usable
    // at runtime — the point is purely the source-generation back-fill at compile time.
    public static void CreateRecord(string name, params (string field, Type type)[] fields) { }
}
";
}

internal sealed record RecordSpec(string Name, ImmutableArray<FieldSpec> Fields);

internal sealed record FieldSpec(string Name, string Type);
